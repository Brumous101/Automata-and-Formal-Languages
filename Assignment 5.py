class DFA(object):
    def __init__(self,state_set,sigma,delta,start_state,state_set_final):
        self.sigma = sigma
        self.state_set = state_set
        self.delta = delta
        self.start_state = start_state
        self.state_set_final = state_set_final
        self.state=self.start_state
    def process_string(self,instr):
        for c in instr:
            if c not in self.sigma:
                return False
            self.state=self.delta[(self.state,c)]
            if self.state not in self.state_set:
                return False
        if self.state in self.state_set_final:
            return True
        return False
    def reset(self):
        self.state=self.start_state
    def state_accepting(self):
        if self.state in self.state_set_final:
            return True
        return False 

